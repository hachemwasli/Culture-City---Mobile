package com.mycompany.Entity;

/**
 *
 * @author Zliaa
 */
public class Image {
    private int id ;
    private String path;

    public Image() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    @Override
    public String toString() {
        return "Image{" + "id=" + id + ", path=" + path + '}';
    }
    
    
    
}
